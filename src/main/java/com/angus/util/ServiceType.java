package com.angus.util;

public class ServiceType {

    public String id;
    private String value;

    public ServiceType(String value){
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
